                <p>Ciudad de México, 29 de marzo de 2023. TecNM/DCD. El Tecnológico Nacional de México estableció una alianza institucional con la Junta Local de Conciliación y Arbitraje de la Ciudad de México, que permitirá la inserción de estudiantes de servicio social y residencias profesionales en el tribunal laboral. </p>

                <p>En la firma del convenio de colaboración, participaron por parte del organismo público, su presidente, maestro Eleazar Rubio Aldarán, y el ingeniero Oscar Castellanos Hernández, director del Tecnológico Nacional de México Campus Gustavo A. Madero II. </p>
                

                <p>Esta cooperación es trascendente para la inserción de estudiantes del ITGAMII en materia de servicio social y residencias profesionales, particularmente de la carrera de Ingeniería en Administración, en cuya retícula se imparte la materia de Derecho Laboral, destacó el ingeniero Castellanos Hernández.</p>
            

                <p>Explicó que esta rama de las ciencias jurídicas estudia todo lo relativo a las relaciones trabajadores y patrones, y las controversias que se dirimen a través de los tribunales de conciliación y arbitraje. Este conocimiento es adquirido por los estudiantes en Administración como parte de la especialidad de Gestión del Capital Humano, sin dejar de ser trascendental para toda la comunidad del tecnológico, precisó. </p>

                <p>Por lo cual, el titular de esta casa de estudios ofreció programas de capacitación y talleres en materia de Relaciones Laborales, con la finalidad de que los futuros profesionistas se encuentren preparados para actuar en un mercado laboral cada vez más demandante. </p>

                <p>En tanto, el Mtro. Eleazar Rubio Aldarán se comprometió con los jóvenes del ITGAMII para otorgar experiencia en materia legal a través de charlas, conferencias y visitas, permitiendo que los estudiantes conozcan de primera mano los procesos y las técnicas que se utilizan en la mediación de conflictos laborales. </p>
                

