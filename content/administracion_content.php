                <h2>Objetivo General</h2>
                <p>Formar profesionales competentes en la creación, dirección e innovación de organizaciones, que
                    enfrenten desafíos en un entorno globalizado, con visión emprendedora, ética, humana y de
                    responsabilidad social.</p>

                <h2>Perfil de Egreso</h2>
                <ol>

                    <li>Ser un profesional ético, sensible a la diversidad cultural, con la capacidad de gestionar,
                        crear, desarrollar e innovar organizaciones competitivas con una visión de sustentabilidad y
                        responsabilidad social.</li>
                    <li>Ser un emprendedor que promueva la transformación económica y social a través de la creación de
                        empresas, identificando las oportunidades de negocios en contextos locales, regionales,
                        nacionales e internacionales.</li>
                    <li>Proponer soluciones creativas que generen ventajas competitivas para resolver problemas de las
                        áreas funcionales en las organizaciones, utilizando procedimientos, técnicas y herramientas de
                        la ingeniería.</li>
                    <li>Actuar como agente de cambio en las organizaciones, en respuesta a las necesidades del entorno.
                    </li>
                    <li>Utilizar las tecnologías de la información y comunicación en el ejercicio de su práctica
                        profesional.</li>
                    <li>Organizar, integrar y dirigir equipos de trabajo para favorecer el crecimiento de la
                        organización.</li>
                    <li>Aplicar habilidades directivas para el logro de los objetivos organizacionales en diferentes
                        contextos.</li>
                    <li>Utilizar procesos de comunicación efectivos en las organizaciones y su entorno.</li>
                    <li>Interpretar y gestionar los sistemas de calidad y el proceso de mejora continua para las
                        organizaciones.</li>
                    <li>Interpretar y aplicar el marco legal vigente, acorde a las necesidades situacionales de la
                        organización.</li>
                    <li>Desarrollar y/o dirigir proyectos empresariales utilizando los medios y herramientas de forma
                        multidisciplinaria.</li>
                    <li>Diseñar e implementar estrategias para optimizar los procesos de las áreas funcionales de la
                        organización.</li>
                    <li>Tomar decisiones en las operaciones económicas, administrativas y financieras que se generen
                        dentro de la organización, en un marco jurídico/fiscal.</li>
                    <li>Prestar servicios de consultoría en su ámbito profesional.</li>
                    <li>Generar, promover y dirigir investigación para propiciar el desarrollo del entorno.</li>
                    <li>Participar en la administración de sistemas de logística.</li>
                    <li>Identificar y administrar los riesgos financieros de las organizaciones.</li>
                    <li>Diseñar, implementar y evaluar los sistemas y modelos administrativos, para la optimización de
                        los recursos que intervienen en la organización, con un enfoque de calidad y competitividad.
                    </li>
                    <li>Promover y desarrollar el potencial del capital humano en los diferentes niveles de la
                        organización.</li>
                    <li>Diseñar y gestionar procesos productivos y de servicios que respondan a las necesidades del
                        entorno.</li>
                </ol>

