                        <li>Desarrolla e implementa sistemas de información para la gestión de procesos y apoyo en la
                            toma de decisiones, utilizando metodologías basadas en estándares internacionales.</li>
                        <li>Diseña, desarrolla y gestiona sistemas de bases de datos para garantizar la integridad,
                            disponibilidad y confidencialidad de la información.</li>
                        <li>Integra soluciones de sistemas de comunicación con diferentes tecnologías, plataformas o
                            dispositivos.</li>
