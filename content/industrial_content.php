            <h2>Objetivo General</h2>
            <p>Formar profesionistas, en el campo de la ingeniería industrial, líderes, creativos y emprendedores con visión sistémica, capacidad analítica y competitiva que les permita diseñar, implementar, mejorar, innovar, optimizar y administrar sistemas de producción de bienes y servicios en un entorno global, con enfoque sustentable, ético y comprometido con la sociedad.</p>
            
            <h2>Perfil de Egreso</h2>
            <ol>
                
                <li>Analizar, diseñar y gestionar sistemas productivos desde la provisión  de insumos hasta la entrega de bienes  y servicios, integrándolos con eficacia y eficiencia.</li>
                <li>Conocer, seleccionar y aplicar tecnologías para optimizar procesos productivos.</li>
                <li>Diseñar, implementar y administrar sistemas de mantenimiento para la eficiencia en la operación de las  instalaciones y equipos.</li>
                <li>Implementar sistemas de gestión de calidad para satisfacer los requerimientos del cliente y partes interesadas.</li>
                <li>Utilizar los instrumentos de medición requeridos en la certificación y/o acreditación con las normas vigentes. Interpretar e implementar estrategias y métodos estadísticos en los procesos organizacionales para la mejora continua.</li>
                <li>Seleccionar y adecuar modelos de calidad y diseño de experimentos en procesos organizacionales para su optimización.
                    </li>
                <li>Gestionar sistemas de seguridad, salud ocupacional y protección al medio ambiente, en industrias de producción y de servicios.</li>
                <li>Identificar necesidades de su entorno y desarrollar investigación aplicada para crear e innovar bienes y servicios.</li>
                <li>Crear y mejorar productos de alto valor agregado bajo los principios de productividad y competitividad.</li>
                <li>Seleccionar e implementar tecnologías de información y  comunicación dentro de la empresa.</li>
                <li>Participar en proyectos de transferencia, desarrollo y adaptación de tecnologías en los sistemas productivos.</li>
                <li>Diseñar, implementar y mejorar sistemas  y estaciones de trabajo considerando factores ergonómicos para optimizar  la producción.</li>
                <li>Participar en la estandarización de operaciones para la transferencia y adaptación de los sistemas productivos.</li>
                <li>Manejar y aplicar las normas y estándares en  el análisis de operaciones de los sistemas de producción.</li>
                <li>Emprender e incubar empresas con base tecnológica, que promueva el desarrollo socioeconómico de una región, así como su constitución legal.</li>
                <li>Formular, evaluar y gestionar proyectos de inversión, que le permita emprender la  creación de unidades productivas de bienes y servicios bajo criterios de competitividad y sustentabilidad.</li>
                <li>Tomar decisiones para la mejora de sistemas productivos y de servicios, fundamentadas en planteamientos y modelos analíticos.</li>
                
                </ol>	
                
